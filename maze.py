def mark_star(arena, position):
	if arena[position[0]][position[1]] == 's' or arena[position[0]][position[1]] == 'g':
		return  # No need to mark if it's the goal or start

	# Create a new row with the '*' character marked
	arena[position[0]] = (
			arena[position[0]][:position[1]] +
			"*" +
			arena[position[0]][position[1] + 1:]
	)

def depth_limited_search(current_state, limit):
	nodes_expanded = 0
	max_search_depth = 0
	frontier = deque()
	frontier.append(current_state)
	max_nodes_stored = 1
	explored = set()
	while frontier:
		current_state = frontier.pop()
		explored.add(current_state)
		if current_state.is_goal():
			final_cost = current_state.cost
			result_arena = arena.copy()
			
			while current_state.parent:
				current_state = current_state.parent
				mark_star(result_arena, current_state.current_position)
			return result_arena, final_cost, nodes_expanded, max_nodes_stored, max_search_depth

		final_cost = current_state.cost + current_state.manhattan_distance()
		if final_cost > limit:
			continue

		elif not current_state.is_cycle():
			temp_frontier = deque()
			for neighbor in current_state.expand():
				temp_frontier.append(neighbor)
			while temp_frontier:
				top_item = temp_frontier.pop()
				frontier.append(top_item)
				max_search_depth = max(top_item.cost, max_search_depth)
			nodes_expanded += 1
			max_nodes_stored = max(len(frontier) + len(explored), max_nodes_stored)
	return [], limit, -1, -1, -1# Replace with return values

