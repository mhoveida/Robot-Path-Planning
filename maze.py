	def is_goal(self):
		return self.current_position == self.goal

	def manhattan_distance(self):
		return abs(self.current_position[0] - self.goal[0]) + abs(self.current_position[1] - self.goal[1])

	def is_cycle(self):
		current_state = self
		grandparent = current_state.parent
		while grandparent:
			if grandparent.current_position == self.current_position:
				return True  # Cycle
			grandparent = grandparent.parent
		return False #No cycle


